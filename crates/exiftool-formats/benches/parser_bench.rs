// Minimal TIFF (little-endian, no IFD entries)
const MINIMAL_TIFF: &[u8] = &[
    0x49, 0x49, // II (little-endian)
    0x2A, 0x00, // magic
    0x08, 0x00, 0x00, 0x00, // IFD offset
    0x00, 0x00, // 0 entries
    0x00, 0x00, 0x00, 0x00, // next IFD
];

// Minimal GIF (1x1)
const MINIMAL_GIF: &[u8] = &[
    0x47, 0x49, 0x46, 0x38, 0x39, 0x61, // GIF89a
    0x01, 0x00, 0x01, 0x00, // 1x1
    0x00, 0x00, 0x00, // flags, bgcolor, aspect
    0x2C, 0x00, 0x00, 0x00, 0x00, // image descriptor
    0x01, 0x00, 0x01, 0x00, 0x00, // 1x1, no flags
    0x02, 0x01, 0x01, 0x00, // LZW min code, block, data, terminator
    0x3B, // trailer
];

// Minimal BMP (1x1)

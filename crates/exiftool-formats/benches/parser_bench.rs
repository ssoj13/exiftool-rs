fn bench_jpeg_parse(c: &mut Criterion) {
    let parser = JpegParser;
    let mut group = c.benchmark_group("jpeg");
    group.throughput(Throughput::Bytes(MINIMAL_JPEG.len() as u64));
    
    group.bench_function("parse_minimal", |b| {
        b.iter(|| {
            let mut cursor = Cursor::new(black_box(MINIMAL_JPEG));
            parser.parse(&mut cursor)
        })
    });
    
    group.finish();
}

